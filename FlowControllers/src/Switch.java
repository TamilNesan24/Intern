
public class Switch {

	public static void main(String[] args) {
		String s = "tijo";
		switch(s)
		{
		case "tijo":
			System.out.println("tijo");
			break;
		case "kama":
			System.out.println("kama");
			break;
		default:
			System.out.println("default");
			break;
		}

	}

}
