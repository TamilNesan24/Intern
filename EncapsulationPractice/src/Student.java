
public class Student {

	private int id;
	private String name;
	private String college;
	
	public int getId()
	{
		return id;
	}
	public String getName()
	{
		return name;
	}
	public String getCollege()
	{
		return college;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public void setCollege(String college)
	{
		this.college = college;
	}
}
