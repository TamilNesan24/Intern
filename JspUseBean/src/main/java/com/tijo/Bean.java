package com.tijo;

public class Bean {
    
	private int id;
	private String name;
	Bean()
	{
		
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public int getId()
	{
		return id;
	}
	public String getName()
	{
		return name;
	}
}
